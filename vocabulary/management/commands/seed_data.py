    def create_courses(self):
        # Clean up duplicate courses from previous seed runs
        for title, keep_pk in [('Корейский с нуля', 1), ('Первые диалоги', 2)]:
            Course.objects.filter(title=title).exclude(pk=keep_pk).delete()
        course1, _ = Course.objects.update_or_create(
            pk=1, defaults={
                'title': 'Корейский с нуля',
                'description': 'Полный курс для начинающих — от алфавита до первых диалогов', 'level': 'beginner', 'order': 1
            }
        )
        self._seed_course1_lessons(course1)

        course2, _ = Course.objects.update_or_create(
            pk=2, defaults={
                'title': 'Первые диалоги',
                'description': 'Учимся строить простые предложения и диалоги', 'level': 'elementary', 'order': 2
            }
        )
        self._seed_course2_lessons(course2)

        self.stdout.write('  [OK] Courses seeded')

